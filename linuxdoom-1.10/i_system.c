void I_Nanosleep(long sec, long nsec) {
    struct timespec ts = {sec, nsec};
    nanosleep(&ts, NULL);
}

void I_Sleep(float delta_t) {
    long sec = floor(delta_t);
    long nsec = (delta_t - sec) * 1e9;
    I_Nanosleep(sec, nsec);
}



