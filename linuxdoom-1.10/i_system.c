//
// I_Error
//
extern boolean demorecording;

void I_Error (char *error, ...)
{
    va_list	argptr;

    // Message first.
    va_start (argptr,error);
    fprintf (stderr, "Error: ");
    vfprintf (stderr,error,argptr);
    fprintf (stderr, "\n");
    va_end (argptr);

    fflush( stderr );

    // Shutdown. Here might be other errors.
    if (demorecording)
	G_CheckDemoStatus();

    D_QuitNetGame ();
    I_ShutdownGraphics();
    
    exit(-1);
}


char *GetDoomWadDir() {
#ifdef NORMALUNIX
    char *dir = getenv("DOOMWADDIR");
    if (!dir) return ".";
    return dir;
#else
    return ".";
#endif
}

char *GetHomeDir() {
#ifdef NORMALUNIX
    char *home = getenv("HOME");
    if (!home) I_Error("Please set $HOME to your home directory");
    return home;
#else
    return "";
#endif
}
